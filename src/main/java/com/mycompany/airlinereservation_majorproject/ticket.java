    private void txtClassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtClassActionPerformed
        // TODO add your handling code here:
        
        int price = Integer.parseInt(txtPrice.getText().trim());
        String type = txtClass.getSelectedItem().toString().trim();
        
        if("Business Class".equals(type))
        {
            int bPrice = price * 2;
            txtPrice.setText(String.valueOf(bPrice));
            
        }
        else if("First Class".equals(type))
        {
            int fPrice = price * 4;
            txtPrice.setText(String.valueOf(fPrice));
        }
        else
        {
            txtPrice.setText(String.valueOf(price));
        }
        
        
        
    }//GEN-LAST:event_txtClassActionPerformed


